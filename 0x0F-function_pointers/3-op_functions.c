/**
 * op_add - return sum of two integers
 * @a: first int
 * @b: second number
 * Return: sum of the integers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - return difference of two integers
 * @a: first int
 * @b: second number
 * Return: difference of the integers
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - return product of two integers
 * @a: first int
 * @b: second number
 * Return: product of the integers
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - return division of two integers
 * @a: first int
 * @b: second number
 * Return: division of the integers
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return sum of two integers
 * @a: first int
 * @b: second number
 * Return: modulus of the integers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
